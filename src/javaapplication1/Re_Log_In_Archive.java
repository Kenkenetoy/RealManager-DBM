    public void Login() {
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + database, "root", "");
            String username1 = UserName.getText();
            String password1 = Password.getText();
            Statement stm = con.createStatement();
            String sql = "select * from realty where Realty_Name='" + username1 + "' and Realty_Password='" + password1 + "'";
            ResultSet rs = stm.executeQuery(sql);
            if (rs.next()) {
                try {
                    Log_In log = new Log_In();
                    log.show();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "ID or Password is incorrect.");
                clear(UserName, Password, null);
            }
            con.close();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void archive_realty() {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + database, "root", "");
            String sql = "update realty set Is_Archived = ? where Realty_ID = " + ID;
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, "Yes");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "This function is functional!!!!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

